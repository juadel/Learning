

# Square Area and perimeter
def Square_area(side):
    return side**2
def Square_peri(side):
    return 4*side
# Triangule area and perimeter
def Trian_area(base, height):
    return (base*height)/2
def Trian_peri(a,b,c):
    return a+b+c
# Circunference area and perimeter
def Circunference_area(radio):
    pi=3.1416
    return pi*(radio**2)
def Circunference_peri(radio):
    pi=3.1416
    return 2*pi*radio
